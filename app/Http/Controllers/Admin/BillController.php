     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $bill = Bill::findOrFail($id);
        foreach($bill->Orders as $order){
            $order->delete();
            $order->Shipping->delete();

        }
